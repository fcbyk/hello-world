# 以下为git命令的选项

# 显示 Git 版本信息
git -v
git --version

# 显示 Git HTML 文档的路径
git --html-path
# 显示 Git 手册页的路径
git --man-path
# 显示 Git info 页面的路径
git --info-path 

# 显示当前的Git可执行文件路径
git --exec-path

# 设置新的Git可执行文件路径
# 当你安装了多个不同版本的Git，并且希望切换使用其中一个版本时
# 可以使用--exec-path选项来指定Git可执行文件的路径。
git --exec-path=/path/to/new/executable/path

# 显示帮助信息
git -h
git --help





