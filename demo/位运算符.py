import pytest

def demo(a, b):
    """
    位运算符
    作用于整数的二进制位：
    & 与、| 或、^ 异或、~ 取反、<< 左移、>> 右移
    注意：Python 整数是无限精度，~a 等价于 -(a+1)
    """
    return (
        a & b,   # 按位与：都为1才为1
        a | b,   # 按位或：有1则为1
        a ^ b,   # 按位异或：不同为1
        ~a,      # 按位取反：翻转所有位

        # 左移 <<
        # 将数字的二进制表示向左移动指定的位数，右侧补0
        a << 2,

        # 右移 >>
        # 将数字的二进制表示向右移动指定的位数，左侧补符号位（正数补0，负数补1）
        a >> 1,
    )


def test():
    # 6:  0110
    # 10: 1010
    # &:  0010(2), |:1110(14), ^:1100(12)
    # ~6 = -7
    assert demo(6, 10) == (2, 14, 12, -7, 24, 3)
    
    # 再测一组
    assert demo(5, 3) == (1, 7, 6, -6, 20, 2)

    # 6 的二进制：0110
    # 左移 2 位：011000 = 24
    # 等价于：6 * 2² = 6 * 4 = 24
    assert 6 << 2 == 24
    
    # 5 的二进制：0101
    # 左移 2 位：010100 = 20
    # 等价于：5 * 2² = 5 * 4 = 20
    assert 5 << 2 == 20
    
    # 6 的二进制：0110
    # 右移 1 位：0011 = 3
    # 等价于：6 // 2 = 3
    assert 6 >> 1 == 3

    # 10 的二进制：1010
    # 右移 2 位：0010 = 2
    # 等价于：10 // 4 = 2
    assert 10 >> 2 == 2

