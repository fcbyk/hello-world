import pytest

def test():
    """
    运算符优先级示例
    ** 幂 > 一元运算 > 乘除 > 加减 > 位移 > 位与^| > 比较 > 逻辑
    括号可改变默认优先级
    """
    assert (
        3 + 4 * 2 ** 2,             # 3 + (4 * 4) = 19
        1 << 2 + 1,                 # + 高于 <<，结果为 1 << 3 = 8
        not True and True or False, # and > not > or 的组合
        5 & 3 | 1                   # & 高于 |，(5&3)=1，1|1=1
    ) == (
        19, 8, False, 1
    )

